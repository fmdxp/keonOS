#ifndef LIBKEX_H
#define LIBKEX_H

#include <stdint.h>

// ELF Types
typedef uint64_t Elf64_Addr;
typedef uint64_t Elf64_Off;
typedef uint16_t Elf64_Half;
typedef uint32_t Elf64_Word;
typedef int32_t  Elf64_Sword;
typedef uint64_t Elf64_Xword;
typedef int64_t  Elf64_Sxword;

#define EI_NIDENT 16

typedef struct {
	unsigned char	e_ident[EI_NIDENT];
	Elf64_Half	e_type;
	Elf64_Half	e_machine;
	Elf64_Word	e_version;
	Elf64_Addr	e_entry;
	Elf64_Off	e_phoff;
	Elf64_Off	e_shoff;
	Elf64_Word	e_flags;
	Elf64_Half	e_ehsize;
	Elf64_Half	e_phentsize;
	Elf64_Half	e_phnum;
	Elf64_Half	e_shentsize;
	Elf64_Half	e_shnum;
	Elf64_Half	e_shstrndx;
} Elf64_Ehdr;

#define ELFMAG0		0x7f
#define ELFMAG1		'E'
#define ELFMAG2		'L'
#define ELFMAG3		'F'

#define ET_EXEC		2
#define ET_DYN		3
#define EM_X86_64	62

// KeonOS Specifics
#define KEX_NOTE_NAME       "KeonOS"
#define KEX_NOTE_TYPE_VERSION 0x1001

struct KexHeader {
    char verify[4];      // "KEX1"
    uint32_t flags;
    uint32_t stack_size;
    uint32_t heap_size;
    uint32_t capabilities;
} __attribute__((packed));

// API
int kex_verify(void* buffer, uint32_t size);
struct KexHeader* kex_get_header(void* buffer, uint32_t size);

#endif
